public class OperadorNot {
    public static void main(String[] args) {
        System.out.println("*** Operador Lógico NOT ! ***");
        // El operador lógico ! invierte el valor logico, es un operador unario
        
        boolean a = false;
        // Operador NOT (invertimos el valor lógico
        var resultado = !a;
        System.out.println("resultado = " + resultado);
    }
}
