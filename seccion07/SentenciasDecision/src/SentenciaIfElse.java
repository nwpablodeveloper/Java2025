public class SentenciaIfElse {
    public static void main(String[] args) {
        System.out.println("*** Sentencia If-ELSE ***");

        var edad = 30;

        if (edad >= 18){
            System.out.println("Eres mayor de edad");
        }else {
            System.out.println("Eres menor de edad");
        }

    }
}
